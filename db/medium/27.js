　『ハマの踊子』。伊豆の踊子にかけたのだろうか。
「コレなら作れるな」
　西田は一目で判断した。
「関節の数を減らして、サスペンションでカバーする構造にしたんだ。駆動部はチェーンじゃなくてベルトに変えたよ」
「ウエイトは？」
　マイケルが訊ねた。
「ソフビで外装が作れれば、三十キロの範囲にはギリギリ収まるさ」
　川崎は鼻をうごめかせた。
　設計はオッケーだろ、と製作担当の西田は納得している。
「じゃぁ、ソッコーで作り始めなきゃ！」
　麻衣は焦りを感じ始めていた。
　すでに七月二十日。十月上旬に行われるロボコンの地区大会まで三カ月を切っている。
「夏休みが勝負だな」
　西田が言った。高専の夏休みは八月上旬から九月下旬までと長い。その間にどれだけ作業できるかで、ロボットの出来が決まる。
「マシン作りは西田ハンと俺らにまかしときぃや」
　玄龍會の広瀬が勢いよく胸を叩いた。高専生の間では、ロボット本体のことをマシンと呼ぶ。
　ロボット製作に関しては、玄龍會にまかせておけば安心だ、と麻衣も思う。
「マシンの制御プログラムは僕が作るよ」
　川崎が言った。このメンバーじゃ僕以外作れるわけないからね、と付け加えると、
「ひとこと余計なんだ、バーロー！」
