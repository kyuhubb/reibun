　空爆により、陸軍が進む道を作る空軍は、本来学生会であるべきかもしれない。
　しかし、会長よりも副会長の多恵の影響力の方が強い学生会は、学校の存続を望んでいない。カンパしてくれたＯＢは、空軍に近い。ただ、ちょっとしっくり来ない気もする。
　そして、海軍。色々な役割があるものの、基本的に制海権を確保し、味方の陸軍の補給路を保つ役割を受け持つ。逆に、敵方の陸軍の補給路を断つのも大切な要素だ。
