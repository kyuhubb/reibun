　学校からの予算は下りていないものの、麻衣が横浜高専のＯＢたちに出した手紙は効果があった。一口二万円のカンパが、二十口集まっている。
　手紙の文面では横浜高専の廃校は伏せている。ただ、マッカーサーに寄せられたいくつもの手紙にある婉曲な表現を参考に、『現状の定員割れを放置していては取り返しのつかないことになります。ロボットコンテストでの活躍により新入生を集めることが、横浜高専にとって最善の策ではないかと考えています』という表現にしたのだ。
　行間の意味をくみとってくれたＯＢたちは、支援の手を差し伸べてくれた。しばらくの間は、運営資金に困ることはない。
　──三軍のうちのひとつは、ＯＢのことかもしれないなぁ。
　マッカーサーには、軍事上の信念があった。三軍──陸軍、海軍、空軍という三つの軍の力が合わさってこそ、軍隊は本来の力を発揮することができる。この思想は、回想記の中でも再三出てくる。戦争にはひとつの軍だけでは勝てない、という考え方には、麻衣も賛成だ。
　この勝利は陸、海、空三面の完全な一体化による戦略思想の勝利であった。三軍一体となったわが軍は、主として海陸空がばらばらに活動することを避けることにより、またできるだけ正面攻撃を避けることによって、戦史におそらく比類のないわずかな損害で敵を崩壊させることができた。
　横浜高専の三軍とはなんだろう。
　──陸軍はロボット部だよね。
