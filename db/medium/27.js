「ねぇ！」
「どーした？」
「もっと飛ばして！」
「アイヨー！」
　第一京浜から、さらに海沿いの産業道路に西田はハンドルを切った。どこまでも続く直線路。前方にクルマの姿は一台もない。
　野太いエキゾーストノートを響かせつつ、ヨンフォアは闇を切り裂いて突っ走る。
　世の中がヘンなら──そんなモンに合わせてたまるかっての！


「みんなの意見を聞いて、いろいろと直してみたんだけど」
　部室に集まったロボット部員の前で、川崎が設計図を広げた。先日のように大学ノートへの殴り書きではなく、大学ノートの十六倍もあるＢ１の用紙に清書されていた。
