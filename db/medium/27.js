回想記に、軍用機の整備の格差が戦争の勝敗を分ける、というくだりがある。マッカーサーいわく、日本軍には整備不足で〝飛べない飛行機〟が多すぎたというのだ。
　私は日本に着いてから、日本本土の各飛行場で発見された約八千機の日本側軍用機を視察したが、どれも九五％から九八％までは完全な姿で、ただ何か小さい部品が足りないという理由だけのために飛べない状態にあった。この八千機が動けたら、日本の戦争努力がどれほど違ったものになったかは計り知れない。
　この飛行機の群れは、整備することとしないことの違いを悲劇的に物語るものであった。（中略）どういうものか日本はこのわずかな違いの線を越えることができなかった。しかし、このわずかな線が勝利と敗北のきわどい違いをもたらすのだ。
　世界最高峰クラスの性能を誇った零戦を擁する日本は、太平洋戦争中盤からは制空権を完全に失っている。マッカーサーは敵方ながら軍人として「惜しい」と評したのである。
　ピットクルーとして玄龍會の役割は重要だ。飛べない飛行機ならぬ、動かないロボットでは戦争に勝てない。ロボット部の人数不足から、西田をはじめとする玄龍會は、実質ロボットの製作も受け持つことになるだろう。これ以上負担を増やすわけにもいかない。
　どうにかして──いい操縦者を見つけないとなぁ。


